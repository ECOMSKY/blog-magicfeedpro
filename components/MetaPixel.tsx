'use client';

import Script from 'next/script';

/**
 * MagicFeedPro-owned Meta Pixel — fires PageView on every blog page so
 * we can build a "blog reader" audience and retarget them with audit
 * offers on Meta Ads. Hard-coded pixel id because the blog is a
 * separate Next deploy and importing env vars across packages was more
 * trouble than it was worth.
 */
const PIXEL_ID = '1407795591007394';

export default function MetaPixel() {
  return (
    <>
      <Script id="mfp-meta-pixel" strategy="afterInteractive">
        {`
          !function(f,b,e,v,n,t,s)
          {if(f.fbq)return;n=f.fbq=function(){n.callMethod?
          n.callMethod.apply(n,arguments):n.queue.push(arguments)};
          if(!f._fbq)f._fbq=n;n.push=n;n.loaded=!0;n.version='2.0';
          n.queue=[];t=b.createElement(e);t.async=!0;
          t.src=v;s=b.getElementsByTagName(e)[0];
          s.parentNode.insertBefore(t,s)}(window, document,'script',
          'https://connect.facebook.net/en_US/fbevents.js');
          fbq('init', '${PIXEL_ID}');
          fbq('track', 'PageView');
        `}
      </Script>
      <noscript>
        {/* eslint-disable-next-line @next/next/no-img-element */}
        <img
          height="1"
          width="1"
          style={{ display: 'none' }}
          alt=""
          src={`https://www.facebook.com/tr?id=${PIXEL_ID}&ev=PageView&noscript=1`}
        />
      </noscript>
    </>
  );
}
